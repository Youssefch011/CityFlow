def tr(dictionary, key, default=None):
    return dictionary.get(key, default if default is not None else BASE.get(key, key))


def language_selector(st):
    if "language" not in st.session_state or st.session_state.language not in LANGUAGES:
        st.session_state.language = "English"
    lang = st.sidebar.selectbox(
        translations[st.session_state.language].get("language_label", "🌐 Language"),
        LANGUAGES,
        index=LANGUAGES.index(st.session_state.language),
    )
    st.session_state.language = lang
    return translations[lang]
